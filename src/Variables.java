public class Variables {
    public static void main(String [] args) {

        //To store data in a location

        /* Boolean -> 1 byte || true or false
        byte -> 1 byte
        short -> 2 bytes
        int -> 4 bytes
        long -> 8 bytes
        float -> 4 bytes
        double -> 8 bytes
        char -> 2 bytes
        String -> depends
         */
        int x; //Declaration
        x = 10; //Assignment
        System.out.println("x = "+x);
        int y = 1234; //Declaration + Assignment = Initialization
        System.out.println("y = "+y);
    }
}
