public class UserExp {
    public static void main(String [] args) {
        //expression = operand & operators
        //operand = values, variables, numbers etc
        //operators = + - * / *
        int num = 1;
        num ++; //increment by 1
        num --; //decrement by 1
        num *= 3; //num = num * 3
        System.out.println(num);
    }
}
